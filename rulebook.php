<b>1. Procedure:</b>
1.1. Go to (link is not yet ready; will update accordingly)Click on the ‘Signup’ button in the navigation bar. 
     Signup with your LDAP ID and password. Click on Submit.
1.2. Now, once your account is successfully created, Log in by clicking on the ‘Login’ button on the Navbar 
     using your LDAP ID and password as credentials. 
1.3. Click on the My Application on the Navbar for filling your preferences and SoP.
1.4. Fill in your preferences, SoP/resume(as per the project requirement) and submit your form. You can give
     a preference to a maximum of 4 projects. You can refill the preferences before the deadline: 12th Decemeber,
     2020, 11:59 PM. Click the Logout button after done.
1.5. Based on your form (and your resume as the case may be if professors ask for it) professors will make 
     their selections. Professors will then call you up for interviews. In case you get selected for more than 
     one project, you will be allocated the one which is your higher preference.
1.6. Selections will be made by the professors by 20th December, 2020.

<b>2. Rules and Guidelines:</b>
2.1. Go through the list of available projects very carefully. Enquire more about the project in case you are 
     not sure about it, and apply for the projects only if you are genuinely interested.
2.2. Once you have been allocated a project, you would be required to send timely reports to your Professor 
     and give feedback to the Department Council after every 15 days. Non-submission of feedback will lead to
      you not receiving certification.
2.3. It is advised you don't apply for a project on your own and adhere to the RUPEE guidelines for better 
     tracking of the work and for certification of the work done.
2.4. We suggest students remain active throughout the duration of their project. The nature of the involvement 
     of each Professor in the project will vary. At the end of the day, your learning from your project will be
     directly proportional to your own involvement. Stay in touch with your guide (Professor and PhD/M.Tech 
     student). In case you have been given some reading material, set deadlines for yourself and get back to 
     your guide.
2.5. We strongly discourage students from abandoning the projects they undertake, as Professors have shown a
     great deal of enthusiasm. It not only paints a bad picture of the student in front of the Professor but 
     also of the entire Department Council, Professors will then be very reluctant to give projects to 
     Undergraduate students in future. We would like students to discuss their issues, if any, with the 
     Professor/PhD or PG student he has been assigned or with somebody from the Department Council before 
     taking any such drastic step. Discontinuation of the project, if at all, should be done only after 
     discussing in detail with the Professor. Such a discontinuation will make you eligible for a review by 
     the Professor for the period of your actual work. You will be penalized according to point 3 if you ditch 
     the project.

<b>3. Incentives and Rewards:</b>
3.1. First-hand exposure to research and the opportunity to learn during the upcoming semesters.
3.2. Certificate of successful participation from HSS Department office only if the professor is satisfied by 
     your work and you submit a good project report.
3.3. Advantages of a good project are manifold –
     a) Getting a good internship at the end of the third year.
     b) Recommendation from your Professor for your good work which might help you in getting an internship 
     in a top university abroad at the end of the second/third year.
     c) Substantial work in summer breaks or the project continued in the next semester can even earn you the 
     Undergraduate Research Award (URA).
     d) Beforehand experience in research might also help you to perform better in your BS project.

<b>4. Penalty:</b>
4.1. If a professor is unsatisfied with your project work and/or you abandon a project opportunity, you will 
not receive the certificate from the HSS Department office.
4.2. Blacklisting from further such programs run by the HSS department council in case of abandoning the 
project without communication about the same.

<b>NOTE:</b> When you apply for projects through RUPEE, it will be assumed that you have gone through the 
RUPEE rule book carefully, and shall be ready to be rewarded or penalised appropriately.


